# Subset: In computer science, a subset refers to a selection of elements from a larger set. 
# For example, for the set {1, 2, 3}, some valid subsets are {1}, {2}, {3}, {1, 2}, {1, 3}, {2, 3}, and {1, 2, 3}.

# Difference with Permutations and Combinations:

# Subsets: Order doesn't matter, so {1, 2} and {2, 1} are considered the same.
# Permutations: Order matters, so {1, 2} and {2, 1} are considered different.
# Combinations: kOrder doesn't matter, but repetitions are not allowed, so {1, 2} and {2, 1} 
# are considered the same, but {1, 1} is not allowed.


